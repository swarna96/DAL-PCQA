#!/usr/bin/env python3
"""
LLM-as-a-judge for evaluating quality of generated PCQA descriptions vs reference.
Uses a Hugging Face chat model (e.g. Llama 3.1 8B Instruct) to score alignment 1–5.

Setup:
  pip install transformers torch accelerate
  # For Llama 3.1: accept license at https://huggingface.co/meta-llama/Llama-3.1-8B-Instruct then huggingface-cli login

Usage:
  python llm_judge_pcqa.py --predictions ../answers/<answers_file> \\
    --test_json ../llava_data/test_pcqa_llava.json \\
    --annotations path/to/<annotation_file> --ref_col Generated_Description \\
    --out_json ../answers/llm_judge_scores.json
  Annotations can be CSV or XLSX (for XLSX: pip install pandas openpyxl).
"""

import argparse
import csv
import json
import math
import re
from typing import Any, Dict, List, Optional, Tuple

try:
    import torch
    from transformers import AutoModelForCausalLM, AutoTokenizer
    TRANSFORMERS_AVAILABLE = True
except ImportError:
    TRANSFORMERS_AVAILABLE = False


def normalize_text(s: str) -> str:
    if not s or not isinstance(s, str):
        return ""
    return re.sub(r"\s+", " ", s.strip())


JUDGE_SYSTEM = """You are an expert judge for point cloud quality assessment (PCQA) descriptions. You score alignment to a reference on a scale of 1 to 5. Always respond with a single line: Score: <number> (and optionally one sentence reason)."""

JUDGE_USER_WITH_REF = """You are an expert evaluating point cloud quality descriptions.

You are given:
1) The ground-truth description generated from human-annotated distortion labels.
2) A candidate description generated by a model.

Distortion taxonomy:
- Texture condition
- Brightness distortion
- Color distortion
- Noise
- Blurriness
- Point density distortion
- Scattering artifact
- Grid artifact
- Missing region
- Deformed shape
- Overall 5-level quality label (excellent, good, fair, poor, bad)

Your task is to judge how well the candidate matches the ground-truth description in terms of:
1. Distortion types (same important categories, no hallucination).
2. Distortion severity (similar levels).
3. Overall quality label (consistent).

Output ONLY a single integer from 1 to 5:
1 = very poor alignment
2 = poor alignment
3 = moderate alignment
4 = good alignment
5 = excellent alignment

Ground-truth description:
<<<
{reference}
>>>

Candidate description:
<<<
{description}
>>>

Now output the score (1–5) only."""


def build_messages(description: str, reference: str) -> List[Dict[str, str]]:
    user = JUDGE_USER_WITH_REF.format(reference=reference, description=description)
    return [
        {"role": "system", "content": JUDGE_SYSTEM},
        {"role": "user", "content": user},
    ]


def parse_score_from_response(text: str) -> Optional[float]:
    if not text:
        return None
    text = text.strip()
    m = re.search(r"[Ss]core\s*:\s*([1-5](?:\.\d+)?)", text, re.IGNORECASE)
    if m:
        try:
            return float(m.group(1))
        except ValueError:
            pass
    m = re.match(r"^([1-5])\.?0?\s*$", text)
    if m:
        try:
            return float(m.group(1))
        except ValueError:
            pass
    m = re.search(r"\b([1-5])(?:\.0)?\s*(?:$|\.|,|\s|/)", text)
    if m:
        try:
            return float(m.group(1))
        except ValueError:
            pass
    return None


def load_predictions(path: str) -> List[Dict]:
    with open(path, encoding="utf-8") as f:
        data = json.load(f)
    if isinstance(data, list):
        return data
    return data.get("answers", data.get("results", [data]))


def _image_path_to_ply(image_path: str) -> str:
    parts = (image_path or "").replace("\\", "/").split("/")
    for p in parts:
        if p.endswith(".ply"):
            return p
    if "/" in image_path:
        return parts[0]
    if image_path.endswith(".png"):
        return image_path.rsplit(".", 2)[0]
    return image_path or ""


def _load_annotations_rows(path: str) -> List[Dict[str, Any]]:
    """Load annotations from CSV or XLSX; return list of row dicts (str keys, str values)."""
    path_lower = path.lower()
    if path_lower.endswith(".csv"):
        with open(path, newline="", encoding="utf-8") as f:
            return list(csv.DictReader(f))
    if path_lower.endswith(".xlsx") or path_lower.endswith(".xls"):
        try:
            import pandas as pd
        except ImportError:
            raise ImportError("XLSX support requires pandas and openpyxl. pip install pandas openpyxl")
        df = pd.read_excel(path, engine="openpyxl" if path_lower.endswith(".xlsx") else None)
        return [
            {str(k): "" if v is None or (isinstance(v, float) and math.isnan(v)) else str(v).strip()
             for k, v in row.items()}
            for row in df.to_dict("records")
        ]
    raise ValueError(f"Annotations file must be .csv or .xlsx, got: {path}")


def load_id_to_ref(
    test_json: str,
    annotations_path: str,
    ply_col: str = "Ply_name",
    ref_col: str = "Generated_Description",
) -> Tuple[Dict[str, str], Dict[str, str]]:
    """Returns (id -> reference_text, id -> ply_name)."""
    with open(test_json, encoding="utf-8") as f:
        test_data = json.load(f)
    id_to_image = {}
    for item in test_data:
        pid = item.get("id")
        path = item.get("image_A") or item.get("image")
        if pid is not None and path:
            id_to_image[pid] = path

    ply_to_ref: Dict[str, str] = {}
    for row in _load_annotations_rows(annotations_path):
        ply = (row.get(ply_col) or "").strip()
        ref = (row.get(ref_col) or "").strip()
        if ply and ref:
            ply_to_ref[ply] = normalize_text(ref)

    id_to_ref: Dict[str, str] = {}
    id_to_ply: Dict[str, str] = {}
    for pid, image_path in id_to_image.items():
        ply_name = _image_path_to_ply(image_path)
        id_to_ply[pid] = ply_name
        if ply_name in ply_to_ref:
            id_to_ref[pid] = ply_to_ref[ply_name]
    return id_to_ref, id_to_ply


def run_judge(
    model_name: str,
    pairs: List[Tuple[str, str, str]],
    max_new_tokens: int = 64,
    temperature: float = 0.0,
    load_in_4bit: bool = False,
    device_map: str = "auto",
) -> List[Dict]:
    if not TRANSFORMERS_AVAILABLE:
        raise RuntimeError("Install: pip install transformers torch accelerate")

    tokenizer = AutoTokenizer.from_pretrained(model_name, trust_remote_code=True)
    model_kwargs = {"torch_dtype": torch.bfloat16 if torch.cuda.is_available() else torch.float32}
    if load_in_4bit:
        try:
            from transformers import BitsAndBytesConfig
            model_kwargs["quantization_config"] = BitsAndBytesConfig(load_in_4bit=True)
        except ImportError:
            pass
    model = AutoModelForCausalLM.from_pretrained(
        model_name,
        device_map=device_map,
        trust_remote_code=True,
        **model_kwargs,
    )
    model.eval()

    results = []
    for i, (sid, description, ref) in enumerate(pairs):
        messages = build_messages(description, ref)
        text = tokenizer.apply_chat_template(
            messages,
            tokenize=False,
            add_generation_prompt=True,
        )
        inputs = tokenizer(text, return_tensors="pt").to(model.device)
        with torch.no_grad():
            out = model.generate(
                **inputs,
                max_new_tokens=max_new_tokens,
                temperature=temperature,
                do_sample=temperature > 0,
                pad_token_id=tokenizer.eos_token_id,
            )
        response = tokenizer.decode(out[0][inputs["input_ids"].shape[1]:], skip_special_tokens=True)
        score = parse_score_from_response(response)
        results.append({
            "id": sid,
            "score": score,
            "raw_response": response.strip(),
            "parsed": score is not None,
        })
        if (i + 1) % 50 == 0:
            print(f"  Judged {i + 1}/{len(pairs)}")

    return results


def main():
    parser = argparse.ArgumentParser(description="LLM-as-a-judge for PCQA description quality")
    parser.add_argument("--predictions", type=str, required=True, help="JSON with id and text (generated descriptions)")
    parser.add_argument("--model", type=str, default="meta-llama/Llama-3.1-8B-Instruct",
                        help="Hugging Face model (e.g. meta-llama/Llama-2-7b-chat-hf)")
    parser.add_argument("--test_json", type=str, required=True, help="Test JSON (id, image_A or image) to map id -> ply -> ref")
    parser.add_argument("--annotations", type=str, required=True,
                        help="Annotations file: CSV or XLSX with reference descriptions (Ply_name, ref_col)")
    parser.add_argument("--ply_col", type=str, default="Ply_name")
    parser.add_argument("--ref_col", type=str, default="Generated_Description")
    parser.add_argument("--max_samples", type=int, default=None, help="Cap number of samples (for debugging)")
    parser.add_argument("--max_new_tokens", type=int, default=64)
    parser.add_argument("--temperature", type=float, default=0.0)
    parser.add_argument("--load_in_4bit", action="store_true", help="Load model in 4-bit (saves VRAM, needs bitsandbytes)")
    parser.add_argument("--out_json", type=str, default=None, help="Save per-sample scores and raw responses")
    args = parser.parse_args()

    preds = load_predictions(args.predictions)
    id_to_ref, _id_to_ply = load_id_to_ref(args.test_json, args.annotations, args.ply_col, args.ref_col)

    pairs: List[Tuple[str, str, str]] = []
    for p in preds:
        pid = p.get("id")
        text = normalize_text(p.get("text", ""))
        if not pid or not text:
            continue
        ref = id_to_ref.get(pid)
        if ref is None:
            continue
        pairs.append((pid, text, ref))

    if args.max_samples:
        pairs = pairs[: args.max_samples]

    if not pairs:
        print("No samples to judge. Check --predictions, --test_json, and --annotations.")
        return

    print(f"Running LLM judge: {args.model} on {len(pairs)} samples (vs reference)")
    results = run_judge(
        args.model,
        pairs,
        max_new_tokens=args.max_new_tokens,
        temperature=args.temperature,
        load_in_4bit=args.load_in_4bit,
    )

    scores = [r["score"] for r in results if r["score"] is not None]
    n_parsed = len(scores)
    if n_parsed:
        mean_score = sum(scores) / n_parsed
        print(f"\nParsed scores: {n_parsed}/{len(results)}")
        print(f"Mean score (1-5): {mean_score:.3f}")
    else:
        print("\nNo scores could be parsed from model outputs. Check --out_json for raw_response.")

    if args.out_json:
        out = {
            "model": args.model,
            "n_samples": len(results),
            "mean_score": sum(scores) / len(scores) if scores else None,
            "results": results,
        }
        with open(args.out_json, "w", encoding="utf-8") as f:
            json.dump(out, f, indent=2, ensure_ascii=False)
        print(f"Results saved to {args.out_json}")


if __name__ == "__main__":
    main()
